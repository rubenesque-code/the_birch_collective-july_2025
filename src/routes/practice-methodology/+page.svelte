					Early intervention can take many forms. The support that someone needs won’t always be
					‘clinical’, and it’s important that we don’t over-medicalise people’s experience of
					distress. The ‘right’ support will depend on someone’s individual needs, how those needs
					affect them, the severity of their symptoms, their individual strengths, and their wider
					circumstances. In some cases, interventions which encourage people to access activities
					which improve mood and wellbeing such as the arts, physical activity, or nature, may be
					most effective.
				</p>

				<p class="mt-4">
					For us at Birch, our early intervention model provides preventative solutions and a
					passion for lifelong learning to enable people to reach their potential. There is a lack
					of transition services between adolescent and adult mental health provision. This age
					group has specific needs and our community mental health model lacks the appearance of a
					service, removing barriers to engagement. With the Green Social Prescribing trial, a
					different approach to mental health provision is developing and services such ours are
					pioneering a nature-based approach, tailored to young adults.
				</p>

				<p class="mt-4">
					Whilst our programmes are suitable for anyone seeking to spend more time outdoors and meet
					new people, our programmes are specifically suitable for those struggling with family and
					relationship issues, struggling to engage with study, socially and emotionally isolated,
					those not in education, employment or training and those experiencing mental health
					concerns. Participants are able to self-refer for attendance, however we do have some key
					referral pathways through organisations in the city, such as 16-25 Independent People, .
					As such, many of our participants may come to us through referrals which include
					significant detail around the participants background, which often includes any mental
					health, special educational need, and family circumstance labels and diagnoses. Whilst to
					provide inclusive practices and accessibility we do take into account any required
					adjustments participants may need, however we also take great care to ensure that we see
					beyond any labels assigned to them throughout their experiences with other services and
					instead see all of our participants as empowered, unique individuals with ownership and
					authority over how they wish to present themselves to us.
