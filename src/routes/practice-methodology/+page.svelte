					Wider Context
				</h3>

				<p class="mt-4">
					On a national level, research suggests that, regardless of individuals histories and
					backgrounds, the years between mid-teens and mid-20s are significantly more vulnerable to
					developing mental health concerns, with around half of all lifetime mental health problems
					found to have started by the mid-teens, and three-quarters by the mid-20s. A <a
						href="https://digital.nhs.uk/data-and-information/publications/statistical/mental-health-of-children-and-young-people-in-england/2017/2017"
						target="_blank"
					>
						survey of the mental health of children and young people in England
					</a>
					found that 12.5% of 5 to 19 year olds had at least one mental health disorder when assessed,
					and 5% met the criteria for 2 or more mental health disorders. We aim to address this at Birch
					through targeting our programmes and services specifically for those between the ages of 16-25.
				</p>

				<p class="mt-4">
					Research also suggests that 16-25 year olds are particularly vulnerable to experiencing
					feelings of loneliness and isolation, and that this in itself can prompt and heighten
					mental health and wellbeing concerns for this age range.
				</p>

				<p class="mt-4">
					The Covid-19 pandemic has significantly contributed to and exasperated feelings of
					loneliness and isolation amongst young people. A survey from the Autumn 2020 cohort of
					16yr olds (now 19-20 yr olds) - found that most participants experienced sudden and
					complete loss of social contact with peers. Those impacted most severely were young people
					with pre-existing mental health conditions; young people who receive additional support
					with learning; those living in households where there is violence and abuse; young carers;
					those living in a family where someone had developed long Covid, and members of some
					minority groups, such as LGBT+
				</p>
				<p class="mt-4">
					Nearly all participants surveyed felt there had not been enough support in terms of mental
					health and well-being for young people. Negative mental health impacts of the pandemic may
					be experienced more severely by those young people already isolated, and more vulnerable
					to risk. A commitment to see all behaviour as communication, combined with recognition of
					the value of the insights to be gained from listening to young people themselves, is
					necessary, and is something which we prioritise at Birch.
				</p>
				<p class="mt-4">
					In addition to this, 16-25 year olds are also particularly vulnerable to mental health and
					wellbeing concerns due to the transitional period to adulthood which they are
					experiencing. Once again, transition periods are even more likely to prove challenging for
					those young people that already accessed traditional mental health services in their
					childhood or adolescence. The point of transition from child and adolescent support
					services for mental health, to adult services, is potentially a time of upheaval for young
					people. Young people may find it difficult to navigate new service settings or to manage
					their mental health and wellbeing following discharge from child and adolescent services,
					especially as the availability and type of support can change dramatically.
				</p>
				<p class="mt-4">
					There are also significant risks of young people disengaging or being lost in the
					transition process. This can result in young adults presenting again in crisis or with a
					greater severity of need later in life. Transitions for vulnerable groups, such as those
					within the criminal justice system, can be particularly problematic. They require close
					engagement with the young person and, where appropriate, their families/carers.
				</p>
