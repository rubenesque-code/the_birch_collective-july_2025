					young people from disadvantaged backgrounds (i.e. low socio-economic status, deprived
					neighbourhoods, etc), and Black Caribbean and students with special educational needs
					status are significantly more likely to be excluded from school, and permanently excluded
					students are more likely to experience worse health and well-being outcomes. A recent
					national study found that permanently excluded pupils reported higher levels of life
					dissatisfaction and had a higher reporting of lifestyle behaviours that negatively impact
					on health. As well as this, school exclusion increased the risk of becoming NEET at the
					age of 19/20, and then remaining economically inactive at the age of 25/26, as well as
					experiencing higher unemployment risk and earning lower wages also at the age of 25/26. In
					2023, the percentage of the population of 16-24 year olds defined as ‘not in education,
					employment or training’ (NEET) was estimated to be 41.6% , an increase from previous
