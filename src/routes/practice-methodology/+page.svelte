					households. Employment deprivation similarly has increased, and the statistics showed that
					around 10% of working age people (29,600) are employment deprived. There is also
					significant disparity between the deprivation levels across Bristol. Bristol continues to
					have deprivation ‘hotspots’ that are amongst some of the most deprived areas in the
					country, yet are adjacent to some of the least deprived areas in the country. This means
					that the inequality gap is substantial, with access to opportunities, experiences and
					services greatly unequal between adjacent areas. Birch aims to address this gap, by
					operating in those areas that are most vulnerable to deprivation. By holding our core
					programmes in these locations, we increase the accessibility of our services to young
